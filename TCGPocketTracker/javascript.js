function addArray(arr, d) {
    let output = []
    for (a in arr) {
        output[a] = (arr[a] + d)
    }
    return output
}
function divideArray(arr, d) {
    let output = []
    for (a in arr) {
        output[a] = (arr[a] / d)
    }
    return output
}
function multiplyArray(arr, m) {
    let output = []
    for (a in arr) {
        output[a] = (arr[a] * m)
    }
    return output
}
function NaNtoArray(arr) {
    for (a in arr) {
        if (isNaN(arr[a])) {
            arr[a] = 0
        }
        return arr
    }
}


